    /// <summary>
    /// POST /media - Create access media
    ///
    /// HOW TO REGISTER DIFFERENT MEDIA TYPES:
    ///
    /// 1. RFID (Wiegand/CODE/HEX):
    ///    - Send only entity_id, central_registry_id, type and description
    ///    - The backend automatically detects the format (Wiegand 123,45678 / CODE / HEX)
    ///    - Example: description = "123,45678" or "HEX: FF FF FF"
    ///
    /// 2. LPR (Vehicle plate):
    ///    - Send entity_id, central_registry_id, type=17, description="ABC1D23"
    ///    - IMPORTANT: also send ns32_0=0 and ns32_1=0
    ///    - Without ns32_0/ns32_1, the backend tries to validate the plate as RFID and returns an error
    ///    - NOTE: The recommended way is to use lpr_enabled=true on the vehicle entity,
    ///      as the backend creates/updates the LPR media automatically
    ///
    /// 3. Facial, Biometrics, etc.:
    ///    - Send entity_id, central_registry_id, type and description
    ///    - To avoid RFID validation, send ns32_0=0 and ns32_1=0
    /// </summary>
    public class CreateMediaRequest
    {
        [JsonPropertyName("entity_id")]
        public uint EntityId { get; set; }

        [JsonPropertyName("central_registry_id")]
        public uint CentralRegistryId { get; set; }

        /// <summary>Media type (see constants MediaType.*)</summary>
        [JsonPropertyName("type")]
        public int Type { get; set; } = MediaType.Wiegand26;

        /// <summary>
        /// Description/value of the media.
        /// - RFID: card code (e.g.: "123,45678" or "HEX: FF FF FF")
        /// - LPR: vehicle plate (e.g.: "ABC1D23")
        /// - Facial/Other: descriptive identifier
        /// </summary>
        [JsonPropertyName("description")]
        public string Description { get; set; } = string.Empty;

        /// <summary>
        /// Numeric value NS32_0 - used internally by the backend to store
        /// binary media data (RFID, converted plate, etc.).
        ///
        /// WHEN TO USE:
        /// - RFID: do not send (the backend calculates from the description)
        /// - LPR/Facial/Other: send 0 to avoid RFID validation
        /// </summary>
        [JsonPropertyName("ns32_0")]
        [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
        public uint? Ns32_0 { get; set; }

        /// <summary>
        /// Numeric value NS32_1 - used internally by the backend to store
        /// binary media data (high part of long IDs).
        ///
        /// WHEN TO USE:
        /// - RFID: do not send (the backend calculates from the description)
        /// - LPR/Facial/Other: send 0 to avoid RFID validation
        /// </summary>
        [JsonPropertyName("ns32_1")]
        [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
        public uint? Ns32_1 { get; set; }

        [JsonIgnore]
        public uint RegistryId
        {
            get => CentralRegistryId;
            set => CentralRegistryId = value;
        }

        [JsonIgnore]
        public int TypeAlias
        {
            get => Type;
            set => Type = value;
        }

        [JsonIgnore]
        public string DescriptionAlias
        {
            get => Description;
            set => Description = value;
        }
    }

