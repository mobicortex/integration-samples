    /// <summary>
    /// JSON converter for boolean fields that the backend returns as int (1/0).
    /// </summary>
    public class BoolIntConverter : JsonConverter<bool>
    {
        public override bool Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
        {
            // Accepts both boolean and integer on read
            if (reader.TokenType == JsonTokenType.True)
                return true;
            if (reader.TokenType == JsonTokenType.False)
                return false;
            if (reader.TokenType == JsonTokenType.Number)
                return reader.GetInt32() != 0;

            throw new JsonException($"Unexpected token type for boolean: {reader.TokenType}");
        }

        public override void Write(Utf8JsonWriter writer, bool value, JsonSerializerOptions options)
        {
            // Writes as boolean true/false
            writer.WriteBooleanValue(value);
        }
    }

    /// <summary>
    /// JSON converter for nullable boolean fields that the backend returns as int (1/0).
    /// </summary>
    public class BoolIntNullableConverter : JsonConverter<bool?>
    {
        public override bool? Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
        {
            // Accepts both boolean and integer on read
            if (reader.TokenType == JsonTokenType.True)
                return true;
            if (reader.TokenType == JsonTokenType.False)
                return false;
            if (reader.TokenType == JsonTokenType.Number)
                return reader.GetInt32() != 0;
            if (reader.TokenType == JsonTokenType.Null)
                return null;

            throw new JsonException($"Unexpected token type for boolean: {reader.TokenType}");
        }

        public override void Write(Utf8JsonWriter writer, bool? value, JsonSerializerOptions options)
        {
            // Writes as boolean true/false or null
            if (value.HasValue)
                writer.WriteBooleanValue(value.Value);
            else
                writer.WriteNullValue();
        }
    }

