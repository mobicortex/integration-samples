    /// <summary>
    /// Wrapper for API call results.
    /// </summary>
    public class ApiResult<T>
    {
        /// <summary>true if the HTTP response was 2xx (success).</summary>
        public bool Success { get; set; }

        /// <summary>
        /// Deserialized response data.
        /// Populated when Success=true. For HTTP 409 (Conflict), the SDK also
        /// attempts to deserialize the body, allowing inspection of the existing record.
        /// </summary>
        public T? Data { get; set; }

        /// <summary>
        /// Descriptive error message.
        /// For HTTP failures: "HTTP {code}" (e.g.: "HTTP 409"). For exceptions: exception message.
        /// null when Success=true.
        /// </summary>
        public string? Message { get; set; }

        /// <summary>Raw HTTP response body (JSON). Useful for debugging.</summary>
        public string? RawResponse { get; set; }

