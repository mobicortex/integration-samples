        public const int BioHikvision = 18;    // Biometrics Hikvision
        public const int BioNiceWego = 19;     // Biometrics Nice Wego
        public const int Facial = 20;          // Facial recognition
        public const int Wiegand26 = 21;       // RFID Card Wiegand 26 bits
        public const int Wiegand34 = 22;       // RFID Card Wiegand 34 bits

        /// <summary>Returns the human-readable name of the media type</summary>
        public static string GetName(int type) => type switch
        {
            RemoteControl => "Remote Control",
            Hcs => "HCS",
            Bio or Bio2 or Bio3 => "Biometrics",
            Keyboard => "Password/Keyboard",
            Mc => "MC Control",
            Lpr => "Plate (LPR)",
            BioHikvision => "Bio Hikvision",
            BioNiceWego => "Bio Nice Wego",
            Facial => "Facial",
            Wiegand26 => "RFID Wiegand 26",
            Wiegand34 => "RFID Wiegand 34",
