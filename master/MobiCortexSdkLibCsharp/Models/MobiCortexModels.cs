    ///
    /// IMPORTANT ABOUT LPR (PLATE):
    /// - Type 17 = LPR (vehicle plate recognition)
    /// - When creating an LPR media manually via POST /media, you must send
    ///   the ns32_0 and ns32_1 fields to prevent the backend from trying to validate the plate
    ///   as if it were an RFID format (Wiegand/CODE/HEX).
    /// - The RECOMMENDED way to create LPR is to use lpr_enabled=true on the vehicle entity,
    ///   as the backend creates/updates the LPR media automatically.
    /// </summary>
    public static class MediaType
    {
        public const int RemoteControl = 0;    // Remote control HT
        public const int Hcs = 1;              // HCS
        public const int Bio = 5;              // Biometrics
        public const int Keyboard = 8;         // Password/Keyboard
        public const int Bio2 = 12;            // Biometrics 2
